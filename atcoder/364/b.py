# 問題文
# H 行 
# W 列のグリッドがあります。グリッドの上から 
# i 番目、左から 
# j 番目のマスをマス 
# (i,j) と表記します。

# マス 
# (i,j) は 
# C 
# i,j
# ​
#   が . のとき空きマスであり、# のとき空きマスではありません。

# 高橋君は現在マス 
# (S 
# i
# ​
#  ,S 
# j
# ​
#  ) におり、
# i=1,2,…,∣X∣ の順に以下のルールに従って行動します。

# X の 
# i 文字目が L のとき、高橋君が現在いるマスの 
# 1 つ左のマスが存在し、そのマスが空きマスならば 
# 1 つ左のマスに移動する。そうでないならば、現在いるマスに留まる。
# X の 
# i 文字目が R のとき、高橋君が現在いるマスの 
# 1 つ右のマスが存在し、そのマスが空きマスならば 
# 1 つ右のマスに移動する。そうでないならば、現在いるマスに留まる。
# X の 
# i 文字目が U のとき、高橋君が現在いるマスの 
# 1 つ上のマスが存在し、そのマスが空きマスならば 
# 1 つ上のマスに移動する。そうでないならば、現在いるマスに留まる。
# X の 
# i 文字目が D のとき、高橋君が現在いるマスの 
# 1 つ下のマスが存在し、そのマスが空きマスならば 
# 1 つ下のマスに移動する。そうでないならば、現在いるマスに留まる。
# 一連の行動を終えた後高橋君がどのマスにいるか出力してください。

# 制約
# 1≤H,W≤50
# 1≤S 
# i
# ​
#  ≤H
# 1≤S 
# j
# ​
#  ≤W
# H,W,S 
# i
# ​
#  ,S 
# j
# ​
#   は整数
# C 
# i,j
# ​
#   は . または #
# C 
# S 
# i
# ​
#  ,S 
# j
# ​
 
# ​
#  = .
# X は L, R, U, D からなる長さ 
# 1 以上 
# 50 以下の文字列
# 入力
# 入力は以下の形式で標準入力から与えられる。

# H 
# W
# S 
# i
# ​
  
# S 
# j
# ​
 
# C 
# 1,1
# ​
 
# C 
# 1,2
