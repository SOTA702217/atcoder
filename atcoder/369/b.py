# 高橋君は、横一列に並んだ 
# 100 個の鍵盤からなるピアノを持っています。 このピアノの左から 
# i 個目の鍵盤のことを鍵盤 
# i と呼びます。

# 高橋君は今から 
# N 回にわたってこのピアノの鍵盤を一つずつ押すことでとある曲を演奏します。 
# i 回目に押す鍵盤は鍵盤 
# A 
# i
# ​
#   であり、それを押す手は 
# S 
# i
# ​
#  = L のとき左手、
# S 
# i
# ​
#  = R のとき右手です。

# 演奏を始める前、高橋君は両手をそれぞれ好きな鍵盤の上に置くことができ、この時点での疲労度は 
# 0 です。 演奏中、片方の手を鍵盤 
# x の上から鍵盤 
# y の上へと動かすと疲労度が 
# ∣y−x∣ 増加します（逆に、手の移動以外で疲労度が増加することはありません）。 なお、ある手である鍵盤を押すためには、その手がその鍵盤の上に置かれている必要があります。

# 演奏が終了した時点での疲労度は最小でいくつになるか求めてください。

# 制約
# 1≤N≤100
# 1≤A 
# i
# ​
#  ≤100
# N,A 
# i
# ​
#   は整数
# S 
# i
